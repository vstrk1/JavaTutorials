public class JavaLesson4_4 {
    public static void main (String[] args) {
        //for (declare iterator; conditional statement; change iterator)
        for (int l=10; l>=1; l--){ //для (даем  10, если л меньше или равно 1 уменьгаем на 1)
            System.out.println(l); //показываем л
        }
     }
}

